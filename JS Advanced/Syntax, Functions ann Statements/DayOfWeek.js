function solve(dayOfWeek){
    if (dayOfWeek == 'Monday') {
        console.log(1);
    }
    else if (dayOfWeek == 'Tuesday') {
        console.log(2);
    }
    else if (dayOfWeek == 'Wednesday') {
        console.log(3);
    }
    else if (dayOfWeek == 'Thursday') {
        console.log(4);
    }
    else if (dayOfWeek == 'Friday') {
        console.log(5);
    }
    else if (dayOfWeek == 'Saturday') {
        console.log(6);
    }
    else if (dayOfWeek == 'Sunday') {
        console.log(7);
    }
    else {
        console.log('error');
    }
}

solve('Monday');